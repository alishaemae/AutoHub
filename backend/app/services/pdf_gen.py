def generate_signable_pdf(kind: str, form_data: dict, out_path: str,
                          doc_number: str = "", doc_date: str = "") -> str:
    from datetime import date
    os.makedirs(os.path.dirname(out_path), exist_ok=True)
    doc = SimpleDocTemplate(out_path, pagesize=A4, topMargin=20*mm,
                            bottomMargin=18*mm, leftMargin=20*mm, rightMargin=20*mm)
    S = []; d = form_data or {}
    doc_date = doc_date or f"«___» {date.today():%m.%Y}"
    doc_number = doc_number or "б/н"
    if kind == "contract":
        for space, align, text in _contract_paragraphs(d, doc_number, doc_date):
            S += [Paragraph(text, _st(9.5, align, 13)), Spacer(1, 6 if space else 3)]
    else:
        fio = d.get("fio") or "____________________"
        series = d.get("passport_series") or "____"
        number = d.get("passport_number") or "______"
        issued = d.get("passport_issued") or "________________________"
        address = d.get("address") or "________________________"
        contract_no = d.get("contract_number") or "______"
        contract_dt = d.get("contract_date") or "__________"
        S += [Paragraph("Генеральному директору ООО «АЗИАН ТРЕЙД 25»", _st(10, TA_RIGHT, 14)),
              Paragraph("Нехорошеву Игорю Александровичу", _st(10, TA_RIGHT, 14)),
              Spacer(1, 20),
              Paragraph("<b>Заявление на возврат денежных средств (депозита)</b>",
                        _st(12, TA_CENTER, 18)),
              Spacer(1, 16),
              Paragraph(
                  f"Я, {fio}, паспорт {series} № {number} выдан: {issued}, адрес регистрации "
                  f"{address}. Прошу Вас произвести возврат денежных средств, оплаченные по "
                  f"договору № {contract_no} от {contract_dt} в качестве депозита для "
                  "осуществления ставок на автомобильном аукционе. Уведомлен, что возврат "
                  "осуществляется исключительно на тот расчетный счет, с которого был оплачен "
                  "данный депозит.", _st(10.5, 4, 16)),
              Spacer(1, 12),
              Paragraph(
                  f"Так же уведомлён, что Агентский договор № {contract_no} от {contract_dt} "
                  "будет прекращён по соглашению сторон с момента поступления данного заявления. "
                  "Доступы к ставкам на автомобили будут ограничены.", _st(10.5, 4, 16)),
              Spacer(1, 30),
              Paragraph(f"Дата: {date.today():%d.%m.%Y}{'&nbsp;'*30}Подпись: ______________",
                        _st(10.5, 0, 16))]
    doc.build(S)
    return out_path
